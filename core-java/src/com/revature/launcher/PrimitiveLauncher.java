package com.revature.launcher;

public class PrimitiveLauncher {

	public static void main(String[] args) {
		// primitive whole numbers
		byte b = 10; // byte is 1 byte
		char c = 'a'; // char is 2 bytes unsigned
		c = 101;
		short s = 200; // short is 2 bytes
		int i = 5; // int is 4 bytes
		long l = 5000000000000000l; // long is 8 bytes

		// decimal numbers
		float f = 25.2f;
		double d = 15.2;

		boolean bool = true;

		
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}
